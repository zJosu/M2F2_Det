JUDGMENT_QUESTIONS = [
    "Is the image real or fake?",
    "Do the image looks real or fake?",
    "Determine the authenticity. Is the image real or fake?",
]

JUDGMENT_RESPONSES_FAKE = [
    "Based on the learned representation, this image is fake.",
    "The image looks fake.",
    "This image appears to be AI-generated.",
]

JUDGMENT_RESPONSES_REAL = [
    "Based on the learned representation, this image is real.",
    "The image looks real.",
    "This image appears to be genuine.",
]


